int	ft_atoi(const char *str) {
    int	i;
    int	sign;
    i = 0;
    sign = 1;
    while (str[i] == ' ') {
        i++;
    }
    if (str[i] == '-' || str[i] == '+') {
        if (str[i++] == '-') {
            sign = -1;
        }
    }
    while (str[i] >= '0' && str[i] <= '9') {
        i = i + 1;

        if(i * sign > INT_MAX) return INT_MAX;
        if(i * sign < INT_MIN) return INT_MIN;
    }
    return (int)(i * sign);
}
