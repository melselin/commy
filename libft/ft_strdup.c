#include "libft.h"
char    *ft_strdup(const char *s)
{
    size_t  len = ft_strlen(s);

    char    *copy = (char *)malloc((len + 1) * sizeof(char));

    if (copy == NULL) {
        return NULL;
    }

    size_t  i = 0;
    while (i < len) {
        copy[i] = s[i];
        i++;
    }
    copy[i] = '\0';

    return copy;
}